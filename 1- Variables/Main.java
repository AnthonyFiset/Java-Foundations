public class Main {

        public static void main(String[] args) {
            
    
        //Primitive Data types: 8 Types, Stores Data, Can only hold 1 value, less memory, fast
        
        //Boolean | Primitive Data | Value: True or False
        
        boolean hello; //Declaration = bolean x;

        //Byte | Primitive Data | Value: -128 to 127

        byte birthday;

        //Short | Primitive Data | Value: -32,768 to 32,767
        short years;

        //Int | Primitive Data | Value: -2 billion to 2 billion
        int money;

        //Long | Primitive Data | Value: -9 quintillion to 9 quintillion
        long billions;

        //Float | Primitive Data | Value: Fractional number up to 6-7 digits ex. 3.141592f
        float pi;

        //Double | Primitive Data | Value: Fractional number up to 15 digits ex. 3.141592653589793
        double helpo;

        //Char | Primitive Data | Value: Single Character/Letter/ASCII value ex. 'f'
        char letter;

        // Assignment = letter = 'h';


        // Reference Data: Unlimited(User Defined), Stores an Address, Could hold more than 1 value, more memory, slower

        
        //String | Reference Data | Value: A sequence of characters

        String sentence = "hello friend"; // Initialization = string x ="h";


        //Calling the variable

        System.out.println("hello my name is: " + sentence);

    }

}